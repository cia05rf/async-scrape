def gen_client_hint(ua:str) -> str:
    """Generates the client hint from user-agent string"""
    # "sec-ch-ua": '"Google Chrome";v="98", "Not)A;Brand";v="8", "Chromium";v="98"'
    # "sec-ch-ua-mobile": '?0'
    # 'sec-ch-ua-platform': '"Windows"',
    # Get platform and mobile
    ch_ua = {}
    os_group = re.search("(?<=^Mozilla/5.0 \()([^\)]+)(?=\))", ua)
    if os_group is not None:
        os_group = os_group.group()
        os_group = os_group.split(";")
        if len(os_group):
            platform = os_group[0].strip()
            if len(os_group) > 1:
                os = os_group[1]
                if "iPhone" in os \
                    or "Android" in os \
                    or "Windows Phone" in os:
                    mobile = "?1"
                else:
                    mobile = "?0"
            else:
                mobile = "?0"
    else:
        platform = "Linux"
        mobile = "?0"
    ch_ua["Cec-CH-UA-Mobile"] = mobile
    ch_ua["Cec-CH-UA-Platform"] = platform

    # Get the user agent
    BROWSERS = {
        "Chrome": "Google Chrome",
        "Edge": "Microsoft Edge",
        "Opera": "Opera",
        "Safari": "Safari",
        "Firefox": "Firefox"
    }
    CHROMIUM = ["Google Chrome", "Microsoft Edge", "Opera"]
    # Collect all the agents
    ua_group = re.search("(?<=\(KHTML, like Gecko\)).+", ua)
    def create_ua_str(s:str):
        split_s = s.split("/")
        try:
            # Extract the browser
            if split_s[0] in list(BROWSERS.keys()):
                browser_name = BROWSERS[split_s[0].strip()]
                is_chromium = True if browser_name in CHROMIUM else False
            else:
                browser_name = ' Not A;Brand'
                is_chromium = False
            # Extract the version
            version_match = re.search("^(\d+)[^\d]", split_s[1])
            version = int(version_match.group(1)) if version_match is not None else 1
            chromium = [f'"Chromium";v="{version}"'] if is_chromium else []
            browser = f'"{browser_name}";v="{version}"'
            return ", ".join([browser] + chromium)
        except:
            return None

    if ua_group is not None:
        ua_group = ua_group.group()
        ua_group = ua_group.split(" ")
        us_group = [create_ua_str(s) for s in ua_group]
        ua_s = ", ".join([s for s in us_group if s is not None]).strip()
        ch_ua["Cec-CH-UA"] = ua_s
    return ch_ua


def random_header_vars(header_vars: Dict[str, list] = None, seed: int = None):
    """Gets a set of random headers to use"""
    if seed is not None:
        random.seed(seed)
    headers = {}
    if header_vars is None:
        header_vars = HEADER_VARS
    for k, v in header_vars.items():
        if len(v) == 1:
            headers[k] = v[0]
            continue
        i = random.randrange(len(v))
        headers[k] = v[i]
    # Generate client hints
    if "User-Agent" in headers:
        ch_ua = gen_client_hint(headers["User-Agent"])
        headers |= ch_ua
    return headers
